from scipy.stats import norm
def ic(data, conf=0.95):
    # altere a função para retornar uma tupla (LI, LS) com os limites do intervalo de confiança
    n = len(data)
    mean = np.mean(data)
    std = np.std(data, ddof=1)
    z = norm.ppf((1 + conf) / 2)
    margem_de_erro = z * (std / np.sqrt(n))
    LI = mean - margem_de_erro
    LS = mean + margem_de_erro
    return (LI, LS)

