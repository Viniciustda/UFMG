"""## Exercício 3

Uma corretora de imóveis desconfia que exista uma diferença entre os preços médios de casas de acordo com o número de pisos (`floors`). Ela deseja comparar casas totalmente térreas (1 piso) e casas com mais de 1 piso. Realize um teste de permutação, respondendo as questões.

1 - Definida as hipóteses:

$$H_0: \mu_{T} = \mu_{\bar{T}}$$

$$H_1: \mu_{T} \neq \mu_{\bar{T}}$$

**2 - Qual a estatística de teste?**
"""



def t_obs(data):
  # deve retornar um valor apenas
  # os filtros aplicados sobre data devem estar dentro da função
  casas_terreas = data[data['floors'] == 1]['price']
  casas_mais_de_um_piso = data[data['floors'] > 1]['price']

  t_obs = casas_terreas.mean() - casas_mais_de_um_piso.mean()

  return t_obs

tobs = t_obs(data)
assert_equal(-53396.48, round(tobs, 2))

"""**3 - Faça o processo de shuffling e responda: Há diferença de preço significativa entre casas térreas e casas com mais de um andar?**"""

def shuffling(data):
    # realiza permutações entre os dados de maneira aleatória
    # atribuindo um preço a uma casa independente do número de andares
    # deve retornar uma tupla com os valores críticos

    casas_terreas = data[data['floors'] == 1]['price']
    casas_mais_de_um_piso = data[data['floors'] > 1]['price']

    # Estatística de teste observada
    t_observed = t_obs(data)

    # Número de permutações
    num_permutations = 1000

    # Inicialize a lista para armazenar as estatísticas de teste permutadas
    t_permuted = []

    # Loop para permutações
    for _ in range(num_permutations):
        # Embaralhe os preços mantendo os índices correspondentes
        prices_shuffled = pd.concat([casas_terreas.sample(frac=1), casas_mais_de_um_piso.sample(frac=1)]).reset_index(drop=True)

        # Crie um novo DataFrame com os preços embaralhados e o mesmo número de andares
        shuffled_data = pd.concat([data['floors'], prices_shuffled], axis=1)

        # Calcule a estatística de teste para as permutações
        t_permuted.append(t_obs(shuffled_data))

    # Calcule os valores críticos
    critical_values = np.percentile(t_permuted, [2.5, 97.5])

    return critical_values

(c_inf, c_sup) = shuffling(data)
assert_equal(c_inf < -116000, True)
assert_equal(c_sup > 116000, True)

"""Por fim, conclua: Há diferença entre os preços de casas térreas e casas não térreas?"""

def resposta():
    t_observed = t_obs(data)

    critical_values = shuffling(data)

    if t_observed < critical_values[0] or t_observed > critical_values[1]:
        return True
    else:
        return False

assert_equal(resposta(), False)
